#include "main.h"
#include <string.h>
#include <stdlib.h>

/**
 * _strcmp - compare two strings
 * @s1: string value 1
 * @s2: string value 2
 *
 * Return: interger value
 */

int _strcmp(char *s1, char *s2)
{
	int res;

	res = strcmp(s1, s2);

	if (res == 0)
	{
		return (res);
	}

	else if (res < 0)
	{
		return (res);
	}
	else
	{
		return (res);
	}
}
